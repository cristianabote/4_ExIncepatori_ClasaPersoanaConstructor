package com.company;

public class Person {
     String name;
     int age;
     String mail;

    public Person() {
        this.name = name;
        this.age = age;
        this.mail = mail;
    }

    public Person(String name, int age, String mail){
        this.name=name;
        this.age=age;
        this.mail=mail;
    }
}
